package gent.timdemey.syzygy;


public class WallInfo {

    public final int    color;
    public final double dist;
    public final int    x;
    public final int    y;

    public WallInfo(int color, double dist, int x, int y) {
        this.color = color;
        this.dist = dist;
        this.x = x;
        this.y = y;
    }
}
